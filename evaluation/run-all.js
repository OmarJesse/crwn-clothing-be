// Run all evaluation suites and write structured artifacts:
//   evaluation/results/results.json   — machine-readable
//   evaluation/results/RESULTS.md     — human-readable summary for thesis use

import { writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

import { runBodyShapeEvaluation, formatReport as fmtBodyShape } from "./body-shape-eval.js";
import { runBodyShapeEvaluationAnsur, formatReport as fmtBodyShapeAnsur } from "./body-shape-eval-ansur.js";
import { runSizeRecommendationEvaluation, formatReport as fmtSizeRec } from "./size-recommendation-eval.js";
import { runPaletteEvaluation, formatReport as fmtPalette } from "./palette-eval.js";
import { formatConfusionMatrix, fmtPct } from "./metrics.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const RESULTS_DIR = join(__dirname, "results");

const stamp = new Date().toISOString();

console.log("=".repeat(70));
console.log("CRWN Fit-Aware Commerce — Evaluation Harness");
console.log(`Run started: ${stamp}`);
console.log("=".repeat(70));
console.log();

const bodyShape = runBodyShapeEvaluation({ n: 2000, seed: 42 });
console.log(fmtBodyShape(bodyShape));
console.log();
console.log();

// Real-data validation on the ANSUR II population (gold tape-measure labels vs.
// classifier on pose-noise-perturbed measurements), plus a noise-sensitivity
// sweep so the thesis can show graceful degradation.
const bodyShapeAnsur = runBodyShapeEvaluationAnsur({ seed: 42, noiseSdCm: 3 });
console.log(fmtBodyShapeAnsur(bodyShapeAnsur));
console.log();
const bodyShapeAnsurSweep = [0, 1, 2, 3, 4, 5].map((sd) => {
  const r = runBodyShapeEvaluationAnsur({ seed: 42, noiseSdCm: sd });
  return { noiseSdCm: sd, accuracy: r.accuracy, macroF1: r.macroF1, samples: r.samples };
});
console.log("Noise-sensitivity sweep (ANSUR II, n = " + bodyShapeAnsur.samples + "):");
console.log("  σ(cm)   Accuracy   Macro-F1");
for (const s of bodyShapeAnsurSweep) {
  console.log(
    `  ${String(s.noiseSdCm).padStart(4)}    ${fmtPct(s.accuracy).padStart(8)}   ${fmtPct(s.macroF1).padStart(8)}`
  );
}
console.log();
console.log();

const sizeRec = runSizeRecommendationEvaluation({
  people: 500,
  productsPerCat: 25,
  categories: 4,
});
console.log(fmtSizeRec(sizeRec));
console.log();
console.log();

const palette = runPaletteEvaluation({ samplesPerBucket: 200, noiseLevel: 25 });
console.log(fmtPalette(palette));
console.log();

// ---------- Persist JSON ----------
mkdirSync(RESULTS_DIR, { recursive: true });
const results = {
  generatedAt: stamp,
  bodyShape,
  bodyShapeAnsur,
  bodyShapeAnsurSweep,
  sizeRecommendation: sizeRec,
  palette,
};
writeFileSync(join(RESULTS_DIR, "results.json"), JSON.stringify(results, null, 2));

// ---------- Persist Markdown ----------
const md = [];
md.push("# Evaluation Results");
md.push("");
md.push(`Generated by \`evaluation/run-all.js\` at **${stamp}**.`);
md.push("");
md.push("This document captures the offline metrics computed against synthetic, anthropometrically-realistic data. It is regenerated automatically by CI on every push. For the methodology behind each number see [EVALUATION.md](../../EVALUATION.md).");
md.push("");

md.push("## 1. Body Shape Classification");
md.push("");
md.push(`- Samples: **${bodyShape.samples}** (skipped ${bodyShape.skipped})`);
md.push(`- Accuracy: **${fmtPct(bodyShape.accuracy)}**`);
md.push(`- Macro F1: **${fmtPct(bodyShape.macroF1)}**`);
md.push(`- Weighted F1: **${fmtPct(bodyShape.weightedF1)}**`);
md.push("");
md.push("### Per-class Precision / Recall / F1");
md.push("");
md.push("| Class | Precision | Recall | F1 | Support |");
md.push("|-------|-----------|--------|----|---------|");
for (const r of bodyShape.perClass) {
  md.push(`| ${r.class} | ${fmtPct(r.precision)} | ${fmtPct(r.recall)} | ${fmtPct(r.f1)} | ${r.support} |`);
}
md.push("");
md.push("### Confusion Matrix");
md.push("");
md.push("```");
md.push(formatConfusionMatrix(bodyShape.confusionMatrix));
md.push("```");
md.push("");

md.push("## 1b. Body Shape Classification — REAL DATA (ANSUR II)");
md.push("");
md.push(`Validated against the real **${bodyShapeAnsur.dataset}** survey: ${bodyShapeAnsur.population} subjects with tape-measured girths as gold labels, classifier evaluated on measurements perturbed by zero-mean Gaussian noise (σ = ${bodyShapeAnsur.noiseSdCm} cm) simulating the pose-estimation pipeline.`);
md.push("");
md.push(`- Samples: **${bodyShapeAnsur.samples}**`);
md.push(`- Accuracy: **${fmtPct(bodyShapeAnsur.accuracy)}**`);
md.push(`- Macro F1: **${fmtPct(bodyShapeAnsur.macroF1)}**`);
md.push(`- Weighted F1: **${fmtPct(bodyShapeAnsur.weightedF1)}**`);
md.push("");
md.push("### Per-class Precision / Recall / F1");
md.push("");
md.push("| Class | Precision | Recall | F1 | Support |");
md.push("|-------|-----------|--------|----|---------|");
for (const r of bodyShapeAnsur.perClass) {
  md.push(`| ${r.class} | ${fmtPct(r.precision)} | ${fmtPct(r.recall)} | ${fmtPct(r.f1)} | ${r.support} |`);
}
md.push("");
md.push("### Noise-sensitivity sweep");
md.push("");
md.push("Accuracy and macro-F1 as the simulated pose-estimation measurement noise (σ, cm) increases — shows graceful degradation on real bodies.");
md.push("");
md.push("| σ (cm) | Accuracy | Macro F1 |");
md.push("|--------|----------|----------|");
for (const s of bodyShapeAnsurSweep) {
  md.push(`| ${s.noiseSdCm} | ${fmtPct(s.accuracy)} | ${fmtPct(s.macroF1)} |`);
}
md.push("");
md.push("### Confusion Matrix");
md.push("");
md.push("```");
md.push(formatConfusionMatrix(bodyShapeAnsur.confusionMatrix));
md.push("```");
md.push("");

md.push("## 2. Size Recommendation");
md.push("");
md.push(`- Sample size: **${sizeRec.overall.samples}** (person × product pairs)`);
md.push(`- Top-1 Accuracy: **${fmtPct(sizeRec.overall.top1)}**`);
md.push(`- Top-2 Accuracy: **${fmtPct(sizeRec.overall.top2)}**`);
md.push(`- Top-3 Accuracy: **${fmtPct(sizeRec.overall.top3)}**`);
md.push(`- Mean Reciprocal Rank: **${sizeRec.overall.mrr}**`);
md.push("");
md.push("### By Category");
md.push("");
md.push("| Category | n | Top-1 | Top-2 | Top-3 | MRR |");
md.push("|----------|---|-------|-------|-------|-----|");
for (const [cat, m] of Object.entries(sizeRec.byCategory)) {
  md.push(`| ${cat} | ${m.samples} | ${fmtPct(m.top1)} | ${fmtPct(m.top2)} | ${fmtPct(m.top3)} | ${m.mrr} |`);
}
md.push("");

md.push("## 3. Palette Bucket Classification");
md.push("");
md.push(`- Samples: **${palette.samples}** (noise ±${palette.noiseLevel} per RGB channel)`);
md.push(`- Accuracy: **${fmtPct(palette.accuracy)}**`);
md.push(`- Macro F1: **${fmtPct(palette.macroF1)}**`);
md.push(`- Weighted F1: **${fmtPct(palette.weightedF1)}**`);
md.push("");
md.push("### Per-class Precision / Recall / F1");
md.push("");
md.push("| Class | Precision | Recall | F1 | Support |");
md.push("|-------|-----------|--------|----|---------|");
for (const r of palette.perClass) {
  md.push(`| ${r.class} | ${fmtPct(r.precision)} | ${fmtPct(r.recall)} | ${fmtPct(r.f1)} | ${r.support} |`);
}
md.push("");
md.push("### Confusion Matrix");
md.push("");
md.push("```");
md.push(formatConfusionMatrix(palette.confusionMatrix));
md.push("```");
md.push("");

md.push("---");
md.push("");
md.push("*Synthetic data is drawn from published anthropometric distributions (ANSUR II–derived means and variances). For the real-data validation roadmap (Trendyol catalog + H&M Personalized Fashion Recommendations + DeepFashion via Hugging Face) see [EVALUATION.md §6](../../EVALUATION.md). The Python training-pipeline stub (Keras) is at [evaluation/python/](../python/).*");

writeFileSync(join(RESULTS_DIR, "RESULTS.md"), md.join("\n"));

console.log("=".repeat(70));
console.log(`Wrote evaluation/results/results.json`);
console.log(`Wrote evaluation/results/RESULTS.md`);
console.log("=".repeat(70));
